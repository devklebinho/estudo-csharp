namespace Aula12;

public class Program
{
    public static void Main()
    {
        int i = 0;
        Console.WriteLine("\nIncrentando...");
        while(i<=5)
        {
            Console.WriteLine(i);
            i++;
        }

        int j = 5;
        Console.WriteLine("\nDecrentando...");        
        while(j >= 0)
        {
            Console.WriteLine(j);
            j--;
        }
    }
}